import 'package:flutter/material.dart';

class HelperFunction {
  static getColor(value) {
    if (value == "Green") {
      return Colors.green;
    } else if (value == "Red") {
      return Colors.red;
    } else if (value == "Yellow") {
      return Colors.yellow;
    } else if (value == "Blue") {
      return Colors.blue;
    } else if (value == "Brown") {
      return Colors.brown;
    } else if (value == "Grey") {
      return Colors.grey;
    } else if (value == "Cyan") {
      return Colors.cyan;
    } else if (value == "Teal") {
      return Colors.teal;
    } else if (value == "White") {
      return Colors.white;
    } else if (value == "Black") {
      return Colors.black;
    } else if (value == "Orange") {
      return Colors.orange;
    } else if (value == "Pink") {
      return Colors.pink;
    } else if (value == "Purple") {
      return Colors.purple;
    } else if (value == "Indigo") {
      return Colors.indigo;
    } else if (value == "OrangeAccent") {
      return Colors.orangeAccent;
    }
  }
}
